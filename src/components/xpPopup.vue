<script>
import { mapActions, mapState } from 'vuex'
import XPProgress from 'vue-simple-progress'
import Gsap from 'gsap'

export default {
  components: {
    XPProgress
  },
  props: {
    xp: null,
    xp2: null
  },

  data() {
    return {
      refreshVar: 0,
      v_buttonOK: false,
      xpMax: null,
      currentXP: null,
      tweenedNumber: 0,
      v_lvlUpPopup: false,
      v_coin: false,
      triggerLevelUp: false
    }
  },
  computed: {
    animatedNumber() {
      return this.tweenedNumber.toFixed(0)
    },
    ...mapState('userData', ['user_data'])
  },

  watch: {
    // currentXP(newValue) {
    //   Gsap.To(this.$data, {
    //     tweenedNumber: newValue,
    //     duration: 3.5
    //   })
    // }
    currentXP(newValue) {
      Gsap.fromTo(
        this.$data,
        { tweenedNumber: this.xp2 },
        {
          tweenedNumber: newValue,
          duration: 2
        }
      )
    }
  },
  created() {
    this.currentXP = this.xp2
    this.xpMax = this.user_data.xpToReach
    this.addXp()
  },
  mounted() {},

  methods: {
    addXp() {
      this.refreshXP(this.xp)

      setTimeout(() => {
        if (this.xp < this.xpMax) {
          this.currentXP = this.xp
        } else {
          this.currentXP = this.xpMax
          setTimeout(() => {
            this.triggerLevelUp = true
            this.currentXP = this.xp - this.xpMax
            const lvlUpPayload = {
              newXp: this.currentXP
            }
            this.levelUp(lvlUpPayload)
          }, 2000)
          setTimeout(() => {
            this.v_coin = true
          }, 3500)
        }
      }, 10)

      setTimeout(() => {
        this.v_buttonOK = true
      }, 2000)
    },

    animComplete() {
      this.triggerLevelUp = false
    },

    back() {
      this.v_buttonOK = false
      this.xpMax = null
      this.currentXP = null
      this.tweenedNumber = 0
      this.v_lvlUpPopup = false
      this.triggerLevelUp = false
      this.$emit('closed')
    },
    ...mapActions('userData', ['refreshXP', 'levelUp'])
  }
}
</script>

