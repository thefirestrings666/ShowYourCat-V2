<script>
import { mapActions, mapState } from 'vuex'
import XPProgress from 'vue-simple-progress'
import Gsap from 'gsap'

export default {
  components: {
    XPProgress
  },
  props: {
    xp: null,
    xp2: null
  },

  data() {
    return {
      v_buttonOK: false,
      xpMax: null,
      currentXP: null,
      tweenedNumber: 0,
      v_lvlUpPopup: false
    }
  },
  computed: {
    animatedNumber() {
      return this.tweenedNumber.toFixed(0)
    },
    ...mapState('userData', ['user_data'])
  },

  watch: {
    // currentXP(newValue) {
    //   Gsap.To(this.$data, {
    //     tweenedNumber: newValue,
    //     duration: 3.5
    //   })
    // }
    currentXP(newValue) {
      Gsap.fromTo(
        this.$data,
        { tweenedNumber: this.xp2 },
        {
          tweenedNumber: newValue,
          duration: 2,
          onComplete: this.setLevelUp
        }
      )
    }
  },
  created() {
    this.currentXP = this.user_data.xp
    this.xpMax = this.user_data.xpToReach
    this.addXp()
  },
  mounted() {},

  methods: {
    addXp() {
      this.refreshXP(this.xp)

      setTimeout(() => {
        this.currentXP = this.xp
      }, 10)

      // if (this.animatedNumber >= this.xpMax) {
      //   console.log('hit')
      //   this.v_lvlUpPopup = true
      //   this.currentXP -= this.xpMax
      //   const lvlUpPayload = {
      //     newXp: this.currentXP
      //   }
      //   this.levelUp(lvlUpPayload)
      // }

      setTimeout(() => {
        this.v_buttonOK = true
      }, 1000)
    },
    setLevelUp() {
      if (this.animatedNumber >= this.xpMax) {
        console.log('hit')
        this.v_lvlUpPopup = true
        this.currentXP -= this.xpMax
        const lvlUpPayload = {
          newXp: this.currentXP
        }
        this.levelUp(lvlUpPayload)
      }
    },

    back() {
      this.$emit('closed')
    },
    ...mapActions('userData', ['refreshXP', 'levelUp'])
  }
}
</script>

